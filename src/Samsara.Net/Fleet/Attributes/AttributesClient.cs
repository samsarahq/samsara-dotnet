using Samsara.Net;
using Samsara.Net.Core;

namespace Samsara.Net.Fleet.Attributes;

public partial class AttributesClient : IAttributesClient
{
    private RawClient _client;

    internal AttributesClient(RawClient client)
    {
        _client = client;
    }

    /// <example><code>
    /// await client.Fleet.Attributes.UpdateAsync(new UpdateAttributesRequest { Id = "id" });
    /// </code></example>
    public async Task UpdateAsync(
        UpdateAttributesRequest request,
        RequestOptions? options = null,
        CancellationToken cancellationToken = default
    )
    {
        var _headers = await new Samsara.Net.Core.HeadersBuilder.Builder()
            .Add(_client.Options.Headers)
            .Add(_client.Options.AdditionalHeaders)
            .Add(options?.AdditionalHeaders)
            .BuildAsync()
            .ConfigureAwait(false);
        var response = await _client
            .SendRequestAsync(
                new JsonRequest
                {
                    BaseUrl = _client.Options.BaseUrl,
                    Method = HttpMethodExtensions.Patch,
                    Path = string.Format(
                        "fleet/attributes/{0}",
                        ValueConvert.ToPathParameterString(request.Id)
                    ),
                    Headers = _headers,
                    Options = options,
                },
                cancellationToken
            )
            .ConfigureAwait(false);
        if (response.StatusCode is >= 200 and < 400)
        {
            return;
        }
        {
            var responseBody = await response.Raw.Content.ReadAsStringAsync();
            throw new SamsaraClientApiException(
                $"Error with status code {response.StatusCode}",
                response.StatusCode,
                responseBody
            );
        }
    }
}
